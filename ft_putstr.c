#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		i += write(1, "(null)", 6);
	while (str && str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	return (i);
}
